
public class Atalhos {
	public static void main(String[] args) {
		System.out.println("ctrl + espaço = autocompletar");
		System.out.println("ctrl + F11 = executar");
		System.out.println("ctrl + s = salvar");
		System.out.println("ctrl + alt + down = duplica a linha para baixo");
		System.out.println("ctrl + alt + up = duplica a linha para cima");
		System.out.println("ctrl + d = deleta a linha selecionada");
		System.out.println("ctrl + z = desfazer");
		System.out.println("ctrl + y = refazer");
		System.out.println("ctrl + shift + o = import automático");
		// Não funciona em todas as versões
		System.out.println("ctrl + + = zoom in (aumentar)");
		System.out.println("ctrl + - = zoom out (diminuir)");
		System.out.println("ctrl + 3 = Quick Access");
		System.out.println("ctrl + 1 = Correção automática");
		System.out.println("ctrl + / = comentário de uma linha");
		System.out.println("ctrl + shift + / = comentário de múltiplas linhas");
		System.out.println("ctrl + i = identação");
		
	}

}
